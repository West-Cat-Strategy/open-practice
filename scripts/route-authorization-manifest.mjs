export function routeAuthorizationKey(routeEntry) {
  return `${routeEntry.method.toUpperCase()} ${routeEntry.path}`;
}

export function publicRouteSamplePath(path) {
  if (path === "/caldav/*") return "/caldav/principals/sample/";
  return path
    .replaceAll(/\$\{[^}]+\}/g, "sample")
    .replaceAll(/:[A-Za-z0-9_]+/g, "sample")
    .replaceAll("*", "sample");
}
