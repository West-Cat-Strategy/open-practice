const route = (registrar, testFile, method, path, auth) => ({
  method,
  path,
  registrar,
  testFile,
  auth,
});

const publicRoute = (registrar, testFile, method, path) =>
  route(registrar, testFile, method, path, { kind: "public" });

const tokenRoute = (registrar, testFile, method, path, tokenScope) =>
  route(registrar, testFile, method, path, { kind: "token", tokenScope });

const authRoute = (registrar, testFile, method, path, resource, action, matterScope = "none") =>
  route(registrar, testFile, method, path, {
    kind: "authenticated",
    resource,
    action,
    matterScope,
  });

